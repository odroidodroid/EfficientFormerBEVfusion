class FFN(nn.Module):
    def __init__(self,
                 in_channels,
                 heads,
                 head_conv=64,
                 final_kernel=1,
                 init_bias=-2.19,
                 conv_cfg=dict(type='Conv1d'),
                 norm_cfg=dict(type='BN1d'),
                 bias='auto',
                 **kwargs):
        super(FFN, self).__init__()

        self.heads = heads
        self.init_bias = init_bias
        for head in self.heads:
            classes, num_conv = self.heads[head]

            conv_layers = []
            c_in = in_channels
            for i in range(num_conv - 1):
                conv_layers.append(
                    ConvModule(
                        c_in,
                        head_conv,
                        kernel_size=final_kernel,
                        stride=1,
                        padding=final_kernel // 2,
                        bias=bias,
                        conv_cfg=conv_cfg,
                        norm_cfg=norm_cfg))
                c_in = head_conv

            conv_layers.append(
                build_conv_layer(
                    conv_cfg,
                    head_conv,
                    classes,
                    kernel_size=final_kernel,
                    stride=1,
                    padding=final_kernel // 2,
                    bias=True))
            conv_layers = nn.Sequential(*conv_layers)

            self.__setattr__(head, conv_layers)

    def init_weights(self):
        """Initialize weights."""
        for head in self.heads:
            if head == 'heatmap':
                self.__getattr__(head)[-1].bias.data.fill_(self.init_bias)
            else:
                for m in self.__getattr__(head).modules():
                    if isinstance(m, nn.Conv2d):
                        kaiming_init(m)

    def forward(self, x):
        """Forward function for SepHead.
        Args:
            x (torch.Tensor): Input feature map with the shape of
                [B, 512, 128, 128].
        Returns:
            dict[str: torch.Tensor]: contains the following keys:
                -reg ï¼ˆtorch.Tensor): 2D regression value with the \
                    shape of [B, 2, H, W].
                -height (torch.Tensor): Height value with the \
                    shape of [B, 1, H, W].
                -dim (torch.Tensor): Size value with the shape \
                    of [B, 3, H, W].
                -rot (torch.Tensor): Rotation value with the \
                    shape of [B, 1, H, W].
                -vel (torch.Tensor): Velocity value with the \
                    shape of [B, 2, H, W].
                -heatmap (torch.Tensor): Heatmap with the shape of \
                    [B, N, H, W].
        """
        ret_dict = dict()
        for head in self.heads:
            ret_dict[head] = self.__getattr__(head)(x)

        return ret_dict
    
    
class Pooling(nn.Module):
    """
    Implementation of pooling for PoolFormer
    --pool_size: pooling size
    """
    def __init__(self, pool_size=3):
        super().__init__()
        self.pool = nn.AvgPool2d(
            pool_size, stride=1, padding=pool_size//2, count_include_pad=False)

    def forward(self, x):
        return self.pool(x) - x


class PoolFormerBlock(nn.Module):
    def __init__(self, dim, hidden_dim=256, dropout=0.0, activation="gelu", norm_layer = nn.GroupNorm):
        super().__init__()

        orig = True
        self.norm1 = norm_layer(1, dim) if orig else nn.LayerNorm(dim)

        pool_size = 3
        self.token_mixer = Pooling(pool_size)

        self.norm2 = norm_layer(1, dim) if orig else nn.LayerNorm(dim)

        self.activation = None
        if activation == "relu":
            self.activation=nn.ReLU
        if activation == "gelu":
            self.activation=nn.GELU
        if activation == "glu":
            self.activation=nn.GLU
        if self.activation is None:
            raise RuntimeError(F"activation should be relu/gelu, not {activation}.")

        # Original Poolformer, same as linear if kernel_size==1
        self.mlp = nn.Sequential(
            nn.Conv2d(dim, hidden_dim, 1),
            self.activation(),
            nn.Conv2d(hidden_dim, dim, 1),
            nn.Dropout(dropout),
        ) if orig\
        else nn.Sequential(
            nn.Linear(dim, hidden_dim),
            self.activation(),
            nn.Dropout(dropout),
            nn.Linear(hidden_dim, dim),
            nn.Dropout(dropout),
        )

        # The following two techniques are useful to train deep PoolFormers.
        # self.drop_path
        # self.use_layer_scale

    def forward(self, x):
        x = x + self.token_mixer(self.norm1(x))
        tmp = self.norm2(x)

        # [4, 128, 200] to [C,H,W]=[128, 4, 200]
        batch_size = tmp.shape[0]
        tmp = tmp.permute(1,0,2)
        # print(tmp.shape)
        tmp = torch.unsqueeze(tmp, 0)
        # print(tmp.shape)
        tmp = self.mlp(tmp)
        tmp = torch.squeeze(tmp, 0)
        tmp = tmp.permute(1,0,2)
        x = x + tmp
        return x


